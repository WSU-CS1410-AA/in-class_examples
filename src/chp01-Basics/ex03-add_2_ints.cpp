#include <iostream>

using namespace std;

/* Prints out the sum of two integer */
int main() {
  int x; //define variable x
  int y; //define variable y

  // Getting the values of both x and y
  cout << "Enter x:\n"; cin >> x;
  cout << "Enter y:\n"; cin >> y;
  
  // Printing the result
  cout << "x + y is " << x + y << endl;
  
  return 0;
}

